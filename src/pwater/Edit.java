            PreparedStatement stmt = con.prepareStatement("UPDATE `watercomp`.`uainfo` SET `Password`=?, `SSN`=?, `Type`=?, `HomeAdr`=? WHERE `uName`=?;") ;
            
    
            stmt.setString(1, jTextField3.getText() );
